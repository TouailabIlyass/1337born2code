void	ft_sort_integer_table(int *tab, int size)
{
	int tmp;
	int i;
	int drapeau;

	drapeau = 1;
	while (drapeau == 1)
	{
		drapeau = 0;
		i = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				tmp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = tmp;
				drapeau = 1;
			}
			i++;
		}
	}
}
