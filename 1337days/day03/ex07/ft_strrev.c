char	*ft_strrev(char *str)
{
	int i;
	int j;
	int tmp;

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i = i + 1;
	i = i - 1;
	while (j < i)
	{
		tmp = str[i];
		str[i] = str[j];
		str[j] = tmp;
		j++;
		i--;
	}
	return (str);
}
