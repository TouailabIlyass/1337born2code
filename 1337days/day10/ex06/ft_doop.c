#include "ft_doop.h"

int	ft_add_i(int a, int b)
{
	return (a + b);
}

int	ft_sub_i(int a, int b)
{
	return (a - b);
}

int	ft_mult_i(int a, int b)
{
	return (a * b);
}

int	ft_div_i(int a, int b)
{
	return (a / b);
}

int	ft_mod_i(int a, int b)
{
	return (a % b);
}
