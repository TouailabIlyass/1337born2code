int	ft_find_next_prime(int nb)
{
	int c;
	int i;

	if (nb <= 1)
		return (2);
	while (1)
	{
		c = 1;
		i = 1;
		while (++i <= nb / 2)
		{
			if (nb % i == 0)
			{
				c = 0;
				break ;
			}
		}
		if (c == 1)
			return (nb);
		nb++;
	}
}
