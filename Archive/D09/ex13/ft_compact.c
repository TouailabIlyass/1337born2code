int	ft_compact(char **tab, int length)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < length)
	{
		if (tab[i])
		{
			tab[j] = tab[i];
			j++;
		}
		i++;
	}
	return (j);
}
