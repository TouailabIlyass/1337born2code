int	ft_strcmp(char *s1, char *s2)
{
	char c1;
	char c2;

	c1 = s1[0];
	c2 = s2[0];
	while (c1 == c2)
	{
		c1 = *s1++;
		c2 = *s2++;
		if (c1 == '\0')
			break ;
	}
	return (c1 - c2);
}
